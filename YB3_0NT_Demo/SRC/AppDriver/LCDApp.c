/*********************************************************************************************************
** Function name:           PrintChar
**
** Descriptions:            在当前坐标点输出字符
**
** input parameters:        p_cStr：字符指针
** output parameters:       None
** Returned value:          None
**
** Created by:              ZHANG Ye
** Created Date:            20110418
**--------------------------------------------------------------------------------------------------------
** Modified by:
** Modified date:
**--------------------------------------------------------------------------------------------------------
*********************************************************************************************************/
void PrintChar(char * p_cStr)
{
	char l_cParam1,l_cParam2;

    while(*p_cStr != '\0')		//一直显示，直到字符串结束
    {
     	l_cParam1	= *p_cStr++;

		if(l_cParam1 < 0x80)		// 如果是字符
		{
			GUI_PutString8_16A(g_u8PosX, g_u8PosY, l_cParam1);
			if(g_u8PosX < 232)
			{
				g_u8PosX	+= 8;
			}
			else
			{
			  	Enter();			
			}
		}		// 如果是字符
    	else		 				// 如果是汉字
		{									
			l_cParam2 = *p_cStr++;
			if((l_cParam2 == '\0')||(l_cParam2 < 0x80)) 
				return;
		
			GUI_hz( g_u8PosX, g_u8PosY, l_cParam1,l_cParam2) ;


			if(g_u8PosX < 224)
			{
				g_u8PosX	+= 16;
			}
			else
			{
		  		Enter();
			}
       	}		// 如果是汉字
	}				//while(*p_cStr != '\0')
}	

