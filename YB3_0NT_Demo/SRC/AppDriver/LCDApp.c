/*********************************************************************************************************
** Function name:           LCDInit
**
** Descriptions:            LCD初始化
**
** input parameters:        None
** output parameters:       None
** Returned value:          None
**
** Created by:              ZHANG Ye
** Created Date:            20110418
**--------------------------------------------------------------------------------------------------------
** Modified by:
** Modified date:
**--------------------------------------------------------------------------------------------------------
*********************************************************************************************************/
void LCDInit(void)
{
	g_u8PosX		= 0;
	g_u8PosY		= 0;

	P1_MUX_CLR		|= ( 0x00000001 );		     //EMC_A0 引脚与P1.0复用，引脚选择A0功能
    P3_MUX_CLR		|= LCDRST | LCM_LEDCON	;    //复位GPO_01( 1<<1 )|背光控制GPO_14(1<<14)

	EMCControl			= 0x01;           /*  使能外部EMC                 */
    EMCConfig			= 0x00;			 /*  EMC配置寄存器--大小端――小端   *****/	
    EMCStaticConfig2	= 0x00000181;	 /*  静态存储器配置寄存器   0x81--16位 **** 0x00--8位  */
	
	EMCStaticExtendedWait = 6;
    EMCStaticWaitWen2  = 0x0f;			 /*  静态存储器写使能延时寄存器  */
    EMCStaticWaitOen2  = 0x0f;			 /*  静态存储器输出使能延时寄存器*/
    EMCStaticWaitRd2   = 0x0f;			/*  静态存储器读延时寄存器      */
	EMCStaticWaitPage2 = 0x1f; 
    EMCStaticWaitWr2   = 0x0f;			 /*  静态存储器写延时寄存器      */
    EMCStaticWaitTurn2 = 0x0f;			 /*  静态存储器转换延时寄存器    */
		
	P3_OUTP_CLR = LCDRST;		//复位LCD
	P3_OUTP_SET = LCDRST;		 																  
				 																  
	REG_BKG_ON		= LCM_LEDCON;
	REG_BKG_OFF		= LCM_LEDCON;
	REG_BKG_ON		= LCM_LEDCON;
		 																  
	GUI_Initialize();					//初始化LCM 
    GUI_SetColor(1, 0);					// 设置前景色及背景色
   
   	GUI_ClearSCR();
	BackGroundOFF();
}
